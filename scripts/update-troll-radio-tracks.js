// Regenerates the TROLL_RADIO_TRACKS array in index.html from the live
// "Ole Troll" Spotify playlist. Run manually with `node scripts/update-troll-radio-tracks.js`,
// or via .github/workflows/update-troll-radio-tracks.yml on a schedule.
//
// Spotify's IFrame Embed API has no track-listing method (see memory:
// spotify-iframe-api-limitations), so this scrapes the playlist's public
// embed page instead, which ships its track list as a __NEXT_DATA__ JSON
// blob in the raw HTML.

const fs = require('fs');
const path = require('path');

const PLAYLIST_ID = '0ni59E9HbWpAe8Ydnseh4O';
const EMBED_URL = `https://open.spotify.com/embed/playlist/${PLAYLIST_ID}`;
const INDEX_HTML_PATH = path.join(__dirname, '..', 'index.html');
const START_MARKER = '// TROLL_RADIO_TRACKS:START (auto-generated by scripts/update-troll-radio-tracks.js — do not hand-edit)';
const END_MARKER = '// TROLL_RADIO_TRACKS:END';

function escapeJsString(s) {
  return String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

async function fetchTracklist() {
  const res = await fetch(EMBED_URL, {
    headers: { 'User-Agent': 'Mozilla/5.0 (compatible; TrollRunnerTracklistBot/1.0)' },
  });
  if (!res.ok) throw new Error(`Failed to fetch playlist embed: ${res.status} ${res.statusText}`);
  const html = await res.text();
  const match = html.match(/<script id="__NEXT_DATA__" type="application\/json">(.*?)<\/script>/s);
  if (!match) throw new Error('Could not find __NEXT_DATA__ in playlist embed HTML — Spotify may have changed their page structure.');
  const data = JSON.parse(match[1]);
  const trackList = data.props.pageProps.state.data.entity.trackList;
  if (!Array.isArray(trackList) || !trackList.length) throw new Error('Playlist track list was empty or malformed.');
  return trackList.map(t => ({ uri: t.uri, title: t.title, artist: t.subtitle || '' }));
}

function buildArrayBlock(tracks, eol) {
  const lines = tracks.map(t =>
    `  { uri: '${escapeJsString(t.uri)}', title: '${escapeJsString(t.title)}', artist: '${escapeJsString(t.artist)}' },`
  );
  return [
    START_MARKER,
    'const TROLL_RADIO_TRACKS = [',
    ...lines,
    '];',
    END_MARKER,
  ].join(eol);
}

async function main() {
  const tracks = await fetchTracklist();

  const html = fs.readFileSync(INDEX_HTML_PATH, 'utf-8');
  const eol = html.includes('\r\n') ? '\r\n' : '\n';
  const block = buildArrayBlock(tracks, eol);
  const startIdx = html.indexOf(START_MARKER);
  const endIdx = html.indexOf(END_MARKER);
  if (startIdx === -1 || endIdx === -1) {
    throw new Error('Could not find TROLL_RADIO_TRACKS markers in index.html. Has the surrounding code been edited?');
  }
  const before = html.slice(0, startIdx);
  const after = html.slice(endIdx + END_MARKER.length);
  const updated = before + block + after;

  if (updated === html) {
    console.log('Tracklist unchanged (%d tracks).', tracks.length);
    return;
  }
  fs.writeFileSync(INDEX_HTML_PATH, updated);
  console.log('Updated TROLL_RADIO_TRACKS with %d tracks.', tracks.length);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
